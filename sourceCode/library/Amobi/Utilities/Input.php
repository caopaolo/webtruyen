class Amobi_Utilities_Input {

    public static function standardInputString($string, $strip_tags = 1) {
        $return_str = @trim($string);
        if ($strip_tags == 1) {
            $return_str = @strip_tags($return_str);
        }
        $return_str = @html_entity_decode($return_str, ENT_QUOTES, 'UTF-8');
        if (@class_exists('Normalizer')) {
            $return_str = Normalizer::normalize($return_str);
        }

        return $return_str;
    }

