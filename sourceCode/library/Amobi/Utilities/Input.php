    public static function filterStringForTTS($content) {
        // Xu ly filter nhung phan khong can tong hop TTS
        $content = preg_replace('@\<no_tts\>.*?\<\/no_tts\>@su', '', $content);

        $content = preg_replace('/<br[\s\/]*>|<\/p>/', "\n", $content);
        $content = trim(strip_tags(html_entity_decode($content, ENT_QUOTES, 'UTF-8')));
        $regex = "((https?|ftp)\:\/\/)"; // SCHEME
        $regex .= "([a-z0-9+!*(),;?&=\$_.-]+(\:[a-z0-9+!*(),;?&=\$_.-]+)?@)?"; // User and Pass
        $regex .= "([a-z0-9-.]*)\.([a-z]{2,4})"; // Host or IP
        $regex .= "(\:[0-9]{2,5})?"; // Port
        $regex .= "(\/([a-z0-9+\$_-]\.?)+)*\/?"; // Path
        $regex .= "(\?[a-z+&\$_.-][a-z0-9;:@&%=+\/\$_.-]*)?"; // GET Query
        $regex .= "(#[a-z_.-][a-z0-9+\$_.-]*)?"; // Anchor
        $content = preg_replace("/" . $regex . "/", "", $content);
        // Replace multiple (one ore more) line breaks with a single one.
        $content = preg_replace("/[\r\n]+/", "\n", $content);
        // Replace line with only space
        $content = preg_replace("/[\r\n][\s]+[\r\n]/", "\n", $content);

        return $content;
    }

    // Ham xoa bo nhieu khoang trang
    public static function removeMultiSpace($text) {
        $text = preg_replace('/[\t\n\r\0\x0B]/', '', $text);
        $text = preg_replace('/([\s])\1+/', ' ', $text);
        $text = trim($text);
        return $text;
    }

